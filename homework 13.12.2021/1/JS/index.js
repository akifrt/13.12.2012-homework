document.querySelector('.button-Green').onclick = function changeColor(){
	
	document.querySelector('.color-green').style.backgroundColor = "green";
	document.querySelector('.color-purple').style.backgroundColor = "";
	document.querySelector('.color-red').style.backgroundColor = "";
	document.querySelector('.color-orange').style.backgroundColor = "";
	
}

